namespace Builder.Enums;

/// <summary>
/// Represents the connection type.
/// </summary>
public enum ConnectionType
{
    /// <summary>
    /// Wire connection type like cable.
    /// </summary>
    Wire,

    /// <summary>
    /// Wireless connection type like bluetooth.
    /// </summary>
    Wireless,

    /// <summary>
    /// Supports both wire and wireless connection.
    /// </summary>
    BothWireAndWireless
}